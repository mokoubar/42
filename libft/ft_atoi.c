#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	x;
	int	s;

	i = 0;
	x = 0;
	s = 1;
	while (((str[i] <= 13 && str[i] >= 9) || str[i] == 32) && str[i])
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while ((str[i] <= '9' && str[i] >= '0') && str[i])
	{
		x = x * 10 + (str[i] - '0');
		i++;
	}
	return (x * s);
}
